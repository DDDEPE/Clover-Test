package com.test;

public class Test3 {
	//Test3 선언
	public static void main(String[] args) {
		//문장 출력문
		String name = "현민"; // 문자열(String)의 변수명을 현민이라고 선언
		int age = 24; // 정수(int)의 age 값을 24로 선언
		double height = 185.3; // 실수(double)의 height 값을 185.3으로 선언
		
		System.out.println("현민의 이름 : " + name); // 현민의 이름과 name 값을 출력
		System.out.println("현민의 나이 : " + age); // 현민의 나이와 age 값을 출력
		System.out.println("현민의 키 : " + height); // 현민의 키와 height 값을 출력
	}

}
